def get_column_cubes(full_grid_):
    full_grid = full_grid_[:]
    column_cubes = []
    for x in range(5): #each plane
        column = []
        for y in range(5): #each row of planes
            #iterate through each row, getting the yth plane
            #print(y,x)
            column.append(full_grid[y][x])
        #print(column) 
        column_cubes.append(column)
    return column_cubes

def get_diagonal_cubes(full_grid_):
    full_grid=full_grid_[:]
    diagonal_cubes = []
    true_diag=[] #each plane in the true diagonal
    inverse_diag = [] #each plane in the inverse diagonal
    for w in range(5):
        for x in range(5):
            if w==x:
                true_diag.append(full_grid[w][x])
            if w+x == 4:
                inverse_diag.append(full_grid[w][x])
    diagonal_cubes.append(true_diag)
    diagonal_cubes.append(inverse_diag)
    return diagonal_cubes

            
