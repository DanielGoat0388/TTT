    print(" ___________________         ___________________         ___________________         ___________________         ___________________")
    for x in range(5): #plane
        row_string='|'
        for y in range(5): #row in plane
            row  = row_of_planes[y][x] #yth row on the xth plane, 
            for column in row:
                length = len(row_string)
                if (length/28 - length//28)==0.75: #time to add space
                    row_string+= "       |"

                if column =='':
                    row_string+=("___|")
                else:
                    row_string+=('_')
                    row_string+=column
                    row_string+=("_|")
        print(row_string)
        
