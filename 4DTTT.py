def sum_list(list):
    sum=0
    for element in list:
        sum+=element
    #print(sum)
    list_copy  = list[:]
    list_copy2 = list[:]
    list_copy.sort() #elements are ascending 
    list_copy2.sort( reverse = True) #elements are descending 
    if (list_copy == list or list_copy2 ==list) and sum==10: #must be one or the other and sum to 6
        return sum
    else:
        return 0

