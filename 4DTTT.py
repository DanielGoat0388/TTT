def mark(grid,symbol,coordinates):
    """
    grid is (5 by 5) by (5 by 5)
    symbol is single length string
    coordinates in form [w,x,y,z]
    where 
    w is grid row from top to bottom 
    x is grid column from left to right
    y is row from top to bottom
    z is column from left to right
    """
    w,x,y,z = coordinates[0]-1 , coordinates[1]-1,coordinates[2]-1,coordinates[3]-1
    grid[w][x][y][z] = symbol
    return grid

